package me.finalchild.gbcompiler

import me.finalchild.groovybukkit.GBPlugin
import org.codehaus.groovy.ast.ASTNode
import org.codehaus.groovy.ast.ClassNode
import org.codehaus.groovy.control.CompilePhase
import org.codehaus.groovy.control.SourceUnit
import org.codehaus.groovy.transform.AbstractASTTransformation
import org.codehaus.groovy.transform.GroovyASTTransformation

@GroovyASTTransformation(phase = CompilePhase.SEMANTIC_ANALYSIS)
class Transformer extends AbstractASTTransformation {

    @Override
    void visit(ASTNode[] nodes, SourceUnit source) {
        for (ClassNode clazz in source.AST.classes) {
            if (clazz.superClass.name == 'me.finalchild.groovybukkit.GBScript') {
                clazz.superClass = new ClassNode(GBPlugin)
            }
        }
    }

}
